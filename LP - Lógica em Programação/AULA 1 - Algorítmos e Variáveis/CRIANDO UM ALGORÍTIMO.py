# Crie um algoritmo para criação de um programa que instrua o usuário a fazer café.

# Receita:
# 1. Esquente a água. Ela não precisa chegar a ferver: o ponto ideal para desligar o fogo é no estágio das pequenas bolhas&nbsp;(90ºC a 92ºC). Se você puder usar mineral, melhor, mas água filtrada funciona perfeitamente; só não use água diretamente da torneira, por conta do excesso de cloro.
# 2. Escalde o coador de pano com a água quente (dica: se você deixou a água ferver, não se incomode: ela vai chegar à temperatura ideal enquanto você escalda o coador). Descarte a água usada.
# 3. Coloque o pó de café no coador.
# 4. Comece a despejar a água quente no centro do coador e vá fazendo movimento circulares. Isso ajudará a mexer o pó de café e dispensa a necessidade de mexer com a colher. Deixe a água escoar completamente.

